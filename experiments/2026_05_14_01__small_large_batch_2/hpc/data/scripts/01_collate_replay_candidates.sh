#!/bin/bash

# Configuration options
input_file=../combined_evo_data.csv
rep_column=1
max_update_column=2
found_solution_column=8
output_file=../../replay_rep_config.sh
var_name=reps
    
echo "# This file was generated in this directory: $(pwd)" > $output_file
echo "# This file was generated at $(date)" >> $output_file
echo "" >> $output_file
echo "# ${var_name} is an associative array (dictionary)" >> $output_file
echo "#   The keys are the replicate ids to replay" >> $output_file
echo "#   The associated values are the generation a solution was found" >> $output_file
echo "# NOTE: this was generated (and can be regenerated by navigating to data/scripts" >> $output_file
echo "#   And running: $0" >> $output_file
echo "" >> $output_file

echo "declare -A ${var_name}" >> $output_file

#total_updates=0
while read line; do
    found_sol=$(echo "$line" | cut --delimiter=, -f ${found_solution_column})
    if (( $found_sol == 1 )); then
        rep_id=$(echo "$line" | cut --delimiter=, -f ${rep_column})
        echo "Replicate $rep_id is a candidate for replays"
        max_update=$(echo "$line" | cut --delimiter=, -f ${max_update_column})
        #total_updates=$((total_updates + max_update))
        echo "${var_name}[${rep_id}]=${max_update}" >> $output_file
    fi
done <<< $(grep -P "(.+,){$((found_solution_column - 1))}1," $input_file)

chmod u+x $output_file
